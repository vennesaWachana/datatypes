void main() {
  // Integers (int) - for whole numbers
  int age = 30;
  print("My age is $age years old.");

  // Doubles (double) - for decimal numbers
  double pi = 3.14159;
  print("The value of pi is approximately $pi.");

  // Strings (String) - for representing text
  String name = "Bard";
  print("My name is $name.");

  // Lists (List) - for storing collections of items
  List<String> colors = ["red", "green", "blue"];
  print("Primary colors: $colors");

  // Maps (Map) - for storing key-value pairs
  Map<String, String> fruits = {
    "apple": "red",
    "banana": "yellow",
  };
  print("The color of an apple is ${fruits["apple"]}.");
}
